def concat(str1: str, str2: str) -> str:
    """
    Returns the concatenation of two strings.

    Args:
        str1 (str): The first string.
        str2 (str): The second string.

    Returns:
        str: The concatenation of str1 and str2.
    """
    return str1 + str2
